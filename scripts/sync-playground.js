/**
 * Syncs the playground gallery with src/components/*.
 *
 *   - Scaffolds a demo stub (playground/src/demos/<name>.tsx) for every
 *     component that doesn't have one yet. EXISTING demos are never overwritten,
 *     so your hand-written examples are safe to re-run this over.
 *   - Regenerates playground/src/demos/index.ts so the gallery list always
 *     matches the components on disk: new components get registered, deleted
 *     ones get dropped, and the order of already-registered demos is preserved.
 *
 * "Updating a component" (changing its props/styles) needs no action here —
 * Vite HMR picks source changes up live. This script handles the structural
 * sync: which components appear in the gallery at all.
 *
 * Run: node scripts/sync-playground.js
 */

import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(import.meta.dirname, "..");
const COMPONENTS_DIR = path.join(ROOT, "src", "components");
const DEMOS_DIR = path.join(ROOT, "playground", "src", "demos");
const INDEX = path.join(DEMOS_DIR, "index.ts");

// "date-picker" -> "DatePicker"
const pascal = (name) =>
  name
    .split(/[-_]/)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join("");

// "date-picker" -> "Date Picker"
const label = (name) =>
  name
    .split(/[-_]/)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join(" ");

/** Component names = folders under src/components that have a component.json. */
function discoverComponents() {
  if (!fs.existsSync(COMPONENTS_DIR)) return [];
  return fs
    .readdirSync(COMPONENTS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .filter((d) =>
      fs.existsSync(path.join(COMPONENTS_DIR, d.name, "component.json"))
    )
    .map((d) => d.name)
    .sort();
}

/** Order in which demos are currently registered (so we can preserve it). */
function existingOrder() {
  if (!fs.existsSync(INDEX)) return [];
  const src = fs.readFileSync(INDEX, "utf8");
  const order = [];
  const re = /name:\s*["']([^"']+)["']/g;
  let m;
  while ((m = re.exec(src)) !== null) order.push(m[1]);
  return order;
}

function stub(name) {
  const Comp = pascal(name);
  return `// AUTO-GENERATED stub by scripts/sync-playground.js.
// Re-running the sync will NOT overwrite this file — flesh out the demo below.
export default function ${Comp}Demo() {
  return (
    <div className="text-sm text-gray-500 dark:text-gray-400">
      <h2 className="mb-2 font-medium text-gray-900 dark:text-gray-100">${label(name)}</h2>
      <p>
        TODO: write a demo for the <code>${name}</code> component — import it from{" "}
        <code>../../../src/components/${name}</code> and render some examples.
      </p>
    </div>
  );
}
`;
}

function generateIndex(names) {
  const imports = names
    .map((n) => `import ${pascal(n)}Demo from "./${n}";`)
    .join("\n");
  const entries = names
    .map(
      (n) => `  { name: "${n}", label: "${label(n)}", Demo: ${pascal(n)}Demo },`
    )
    .join("\n");
  return `// AUTO-GENERATED by scripts/sync-playground.js — do not edit the list by hand.
// Add/rename a component under src/components, then re-run:
//   node scripts/sync-playground.js
${imports}
import type { DemoEntry } from "./types";

// Each component under src/components is registered here for the gallery list.
export const demos: DemoEntry[] = [
${entries}
];

export type { DemoEntry, DemoProps } from "./types";
`;
}

function main() {
  if (!fs.existsSync(DEMOS_DIR)) {
    console.error(`Demos dir not found: ${path.relative(ROOT, DEMOS_DIR)}`);
    process.exit(1);
  }

  const components = discoverComponents();
  const prevRaw = existingOrder();

  // Keep already-registered demos in their current order, then append newly
  // discovered components (alphabetical). Drop registrations whose component
  // no longer exists.
  const prev = prevRaw.filter((n) => components.includes(n));
  const fresh = components.filter((n) => !prev.includes(n));
  const removed = prevRaw.filter((n) => !components.includes(n));
  const ordered = [...prev, ...fresh];

  const created = [];
  for (const name of ordered) {
    const file = path.join(DEMOS_DIR, `${name}.tsx`);
    if (!fs.existsSync(file)) {
      fs.writeFileSync(file, stub(name), "utf8");
      created.push(name);
    }
  }

  fs.writeFileSync(INDEX, generateIndex(ordered), "utf8");

  console.log("Playground sync complete.");
  console.log(`  components: ${ordered.join(", ") || "(none)"}`);
  if (created.length)
    console.log(`  scaffolded demo stubs: ${created.join(", ")}`);
  if (fresh.length) console.log(`  newly registered: ${fresh.join(", ")}`);
  if (removed.length)
    console.log(`  unregistered (component removed): ${removed.join(", ")}`);
  if (!created.length && !fresh.length && !removed.length)
    console.log("  index.ts refreshed; everything already in sync.");
}

main();
